# Создать текстовый файл (например, programs.txt), в котором каждая строка содержит название одной программы, а затем передать этот файл в качестве аргумента:

# bash

# ./install_programs.sh programs.txt

#!/bin/bash

# Функция для установки программы
install_program() {
    echo "Установка программы: $1"
    # Добавьте здесь команду установки программы
    # Например, для apt-get:
    # sudo apt-get install -y "$1"
}

# Проверяем, передан ли аргумент с текстовым файлом
if [ $# -eq 1 ] && [ -f "$1" ]; then
    # Считываем список программ из текстового файла
    programs=$(cat "$1")
else
    # Иначе считываем список программ из аргументов командной строки
    programs="$@"
fi

# Проверяем, есть ли программы для установки
if [ -z "$programs" ]; then
    echo "Не указан список программ для установки."
    exit 1
fi

# Перебираем каждую программу из списка и устанавливаем её
for program in $programs; do
    install_program "$program"
done